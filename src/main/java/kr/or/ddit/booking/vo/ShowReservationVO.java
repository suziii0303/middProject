package kr.or.ddit.booking.vo;

public class ShowReservationVO {
	private String re_id;
	private String m_subject;
	private String cn_nm;
	private String th_nm;
	private String t_date;
	private String t_time;
	private int mp_total;
	
	
	public String getRe_id() {
		return re_id;
	}
	public void setRe_id(String re_id) {
		this.re_id = re_id;
	}
	public String getM_subject() {
		return m_subject;
	}
	public void setM_subject(String m_subject) {
		this.m_subject = m_subject;
	}
	public String getCn_nm() {
		return cn_nm;
	}
	public void setCn_nm(String cn_nm) {
		this.cn_nm = cn_nm;
	}
	public String getTh_nm() {
		return th_nm;
	}
	public void setTh_nm(String th_nm) {
		this.th_nm = th_nm;
	}
	public String getT_date() {
		return t_date;
	}
	public void setT_date(String t_date) {
		this.t_date = t_date;
	}
	public String getT_time() {
		return t_time;
	}
	public void setT_time(String t_time) {
		this.t_time = t_time;
	}
	public int getMp_total() {
		return mp_total;
	}
	public void setMp_total(int mp_total) {
		this.mp_total = mp_total;
	}
	
	
	
}
