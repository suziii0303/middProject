public class CinemaVO {
	private String cn_id   ;
	private String sh_id   ;
	private String cn_loc  ;
	private String cn_nm   ;
	private String cn_addr ;
	private String cn_tel  ;
	private String cn_map ;
	
	public String getCn_id() {
		return cn_id;
	}
	public void setCn_id(String cn_id) {
		this.cn_id = cn_id;
	}
	public String getSh_id() {
		return sh_id;
	}
	public void setSh_id(String sh_id) {
		this.sh_id = sh_id;
	}
	public String getCn_loc() {
		return cn_loc;
	}
	public void setCn_loc(String cn_loc) {
		this.cn_loc = cn_loc;
	}
	public String getCn_nm() {
		return cn_nm;
	}
	public void setCn_nm(String cn_nm) {
		this.cn_nm = cn_nm;
	}
	public String getCn_addr() {
		return cn_addr;
	}
	public void setCn_addr(String cn_addr) {
		this.cn_addr = cn_addr;
	}
	public String getCn_tel() {
		return cn_tel;
	}
	public void setCn_tel(String cn_tel) {
		this.cn_tel = cn_tel;
	}
	public String getCn_map() {
		return cn_map;
	}
	public void setCn_map(String cn_map) {
		this.cn_map = cn_map;
	}
	
	
}
