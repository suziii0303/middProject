package kr.or.ddit.screening.vo;

public class ScreeningVO {
	private int sci_num     ;
	private String m_id     ;
	private int t_num       ;
	private String m_subject;
	private String th_nm;
	private String t_date ;
	private String t_time ;
	
	
	public int getSci_num() {
		return sci_num;
	}
	public void setSci_num(int sci_num) {
		this.sci_num = sci_num;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public int getT_num() {
		return t_num;
	}
	public void setT_num(int t_num) {
		this.t_num = t_num;
	}
	public String getM_subject() {
		return m_subject;
	}
	public void setM_subject(String m_subject) {
		this.m_subject = m_subject;
	}
	public String getTh_nm() {
		return th_nm;
	}
	public void setTh_nm(String th_nm) {
		this.th_nm = th_nm;
	}
	public String getT_date() {
		return t_date;
	}
	public void setT_date(String t_date) {
		this.t_date = t_date;
	}
	public String getT_time() {
		return t_time;
	}
	public void setT_time(String t_time) {
		this.t_time = t_time;
	}
	
	
}
