package kr.or.ddit.seat_rank.vo;

public class SeatRankVO {
	private int sr_num    ;
	private String sr_nm  ;
	private int sr_price   ;
	
	
	public int getSr_num() {
		return sr_num;
	}
	public void setSr_num(int sr_num) {
		this.sr_num = sr_num;
	}
	public String getSr_nm() {
		return sr_nm;
	}
	public void setSr_nm(String sr_nm) {
		this.sr_nm = sr_nm;
	}
	public int getSr_price() {
		return sr_price;
	}
	public void setSr_price(int sr_price) {
		this.sr_price = sr_price;
	}

	
}
